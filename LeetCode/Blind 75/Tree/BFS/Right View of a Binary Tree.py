#           1
#         /   \
#       2       3
#     /   \    /  \
#   4     5   6    7
# Right view: [1, 3, 7]
#
# Example 2:
#           12
#         /    \
#       7        1
#     /        /   \
#    9       10     5
#    |
#    3
# Right view: [12, 1, 5, 3]
